package com.cjkj.jcb_caizhan.modul.Order_Manager.order.ticket;

/**
 * 竞彩赔率实体
 * Created by 1 on 2018/3/20.
 */
public class OddsEntity {
    String oddsId;
    String oddsFiles;
    String oddsName;
    String oddsValue;

    public String getOddsId() {
        return oddsId;
    }

    public void setOddsId(String oddsId) {
        this.oddsId = oddsId;
    }

    public String getOddsFiles() {
        return oddsFiles;
    }

    public void setOddsFiles(String oddsFiles) {
        this.oddsFiles = oddsFiles;
    }

    public String getOddsName() {
        return oddsName;
    }

    public void setOddsName(String oddsName) {
        this.oddsName = oddsName;
    }

    public String getOddsValue() {
        return oddsValue;
    }

    public void setOddsValue(String oddsValue) {
        this.oddsValue = oddsValue;
    }
}
